package entities;

public class Department {
	private String name;
	
	// Constructors
	
	public Department() {
		
	}
	
	public Department(String name) {
		this.name = name;
	}
	
	// Getters and Setters Methods
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
